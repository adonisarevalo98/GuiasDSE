    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        int id;
        id = Integer.parseInt(jTextField7.getText());
        operaciones operacion = new operaciones();
        Federados federado = null;
        federado = operacion.obtenerFederado(id);

        String nombre;
        String apellido;
        String direccion;
        String email;
        String password;
        String estado;

        nombre = jTextField1.getText();
        apellido = jTextField2.getText();
        direccion = jTextField3.getText();
        email = jTextField4.getText();
        password = jTextField5.getText();
        estado = jTextField6.getText();

        federado.setNombre(nombre);
        federado.setApellido(apellido);
        federado.setDireccion(direccion);
        federado.setEmail(email);
        federado.setPassword(password);
        federado.setEstado(estado);

        operacion.actualizarFederado(federado);
       
        JOptionPane.showMessageDialog(null,nombre + " Actualizado con exito","", WIDTH);
    }//GEN-LAST:event_jButton1ActionPerformed

